/*
Program 5: Write a program to print the ASCII value of individual character from a string (take hardcoded value)
e.g : abcd
output : a= 97,b = 98,c=99,d=100
*/

class Pro5{
        public static void main(String args[]){
        
        char ch1 = 'a';
        char ch2 = 'b';
        char ch3 = 'c';
        char ch4 = 'd';
        
        int ascii1 = ch1;
        int ascii2 = ch2;
        int ascii3 = ch3;
        int ascii4 = ch4;
        
        System.out.println(ch1+" = "+ascii1+","+ch2+" = "+ascii2+","+ch3+" = "+ascii3+","+ch4+" = "+ascii4);
          
        }
 }
