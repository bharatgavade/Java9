/*
Write a program to print following pattern

        * * * *
        * * * *
        * * * *
        * * * *
*/
class Pro4{
        public static void main(String args[]){
        
        for (int x= 0; x<=3; x++){
                for (int y = 0; y<=3; y++){
                        System.out.print("* ");
                }
                System.out.println();
        }
  }
}
