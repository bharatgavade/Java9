/* Write a java program to take a integer between 1 to 7 and display the name of the weekday using
        switch case 
        Input :input ----> 3
        Output : output ----> wedensday */
class Switch{
        public static void main(String args[]){
                int ch = 3;
                
                switch(ch){
                        case 1:
                                System.out.println("Monday");
                                break;
                        case 2:
                                System.out.println("Tuesday");
                                break;
                        case 3:
                                System.out.println("Wedensday");
                                break;
                        case 4:
                                System.out.println("Thursday");
                                break;
                        case 5:
                                System.out.println("Friday");
                                break;
                        case 6:
                                System.out.println("Saturday");
                                break;
                        case 7:
                                System.out.println("Sunday");
                                break;
                }
        }
}
                            
