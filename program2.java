class Operations {

	int num1=15;
	int num2=12;

	public static void main(String[] args){
	
		Operations obj = new Operations();
		System.out.println("Addition is :"+(obj.num1+obj.num2));
		System.out.println("Subtraction is :"+(obj.num1-obj.num2));
		System.out.println("Multiplication is :"+(obj.num1*obj.num2));
		System.out.println("Division is :"+(obj.num1/obj.num2));
		System.out.println("Modulus is :"+(obj.num1%obj.num2));
	}


}
